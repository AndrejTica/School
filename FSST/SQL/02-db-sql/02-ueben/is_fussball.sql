drop database if exists is_fussball;

create database if not exists is_fussball;

grant create,drop, references, select,insert,update,delete on is_fussball.* to is_fussball identified by 'comein';

use is_fussball;

drop table if exists leitet ;
drop table if exists Verwarnung;
drop table if exists Tore;
drop table if exists Spiel;
drop table if exists Kampfrichter;
drop table if exists Stadien;
drop table if exists Spieler;
drop table if exists Mannschaften;

create table Mannschaften(
  Land varchar(40) primary key,
  Code char(3),
  Trainer varchar(30),
  Kontinent varchar(40),
  Gruppe varchar(1)) engine=innodb;

create table Spieler(
  SpielerNr int primary key,
  Name varchar(40),  
  Land varchar(40) references Mannschaften,
  Geburtsdatum datetime,
  Position varchar(20))engine=innodb;

alter table Spieler 
add constraint FK_spieler_mannschaft
foreign key (Land)
references Mannschaften(Land);


create table Stadien(
  Stadt varchar(20) primary key,
  AnzPlaetze int)engine=innodb;

create table Kampfrichter(
  Funktion varchar(20),
  Name varchar(30),
  Nationalitaet char(3),
  KRID int primary key)engine=innodb;

create table Spiel(
  ID int primary key,
  Runde varchar(20),
  Tag datetime,
  MannschaftA varchar(40) references Mannschaften,
  MannschaftB varchar(40) references Mannschaften,
  Stadion varchar(20) references Stadien,
  Zuschauer int )engine=innodb;

alter table Spiel 
add constraint FK_spielA_mannschaft
foreign key (MannschaftA)
references Mannschaften(Land);

alter table Spiel 
add constraint FK_spielB_mannschaft
foreign key (MannschaftB)
references Mannschaften(Land);


alter table Spiel 
add constraint FK_spiel_stadion
foreign key (Stadion)
references Stadien(Stadt);



create table Tore(
  SpielerNr int references Spieler,
  Spiel int references Spiel,
  Minute int,
  Spielsituation varchar(20),
  primary key(SpielerNr, Spiel, Minute))engine=innodb;

alter table Tore 
add constraint FK_tore_spieler
foreign key (SpielerNr)
references Spieler(SpielerNr);

alter table Tore 
add constraint FK_tore_spiel
foreign key (Spiel)
references Spiel(ID);



create table Verwarnung(
  VID int primary key,
  Karte varchar(20),
  Minute int,
  SpielID int references Spiel,
  SpielerNr int references Spieler)engine=innodb;


alter table Verwarnung 
add constraint FK_verwarnung_spieler
foreign key (SpielerNr)
references Spieler(SpielerNr);

alter table Verwarnung 
add constraint FK_verwarnung_spiel
foreign key (SpielID)
references Spiel(ID);




create table leitet(
  SpielID int references Spiel,
  KRID int references Kampfrichter,
  Funktion varchar(20),
  primary key(SpielID,KRID) )engine=innodb;



alter table leitet 
add constraint FK_leitet_spiel
foreign key (SpielID)
references Spiel(ID);


alter table leitet 
add constraint FK_leitet_kampfrichter
foreign key (KRID)
references Kampfrichter(KRID);





